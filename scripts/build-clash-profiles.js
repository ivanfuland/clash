function renderProfile(profile) {
  return [
    "; AUTO-GENERATED by scripts/build-clash-profiles.js. Do not edit directly.",
    `; ${profile.mode}`,
    "",
    ";1、规则基底",
    `;${profile.baseComment}`,
    `clash_rule_base=${RAW_BASE}/${profile.baseFile}`,
    "",
    "",
    ";2、策略组（域名组-节点组）",
    profile.defaultProxyGroup,
    ...domainGroups,
    "",
    ";3、套娃组",
    ...chainGroups,
    "",
    ";4、节点组",
    ...nodeGroups,
    "",
    ";5、规则生成",
    ";GEOSITE/GEOIP 不走 SubConverter ruleset 生成器，直接使用 clash_rule_base 中的原生 Mihomo 规则。",
    "enable_rule_generator=false",
    "overwrite_original_rules=false",
    "",
  ].join(EOL);
}

